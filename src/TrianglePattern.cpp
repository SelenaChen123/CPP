//  Creates a triangle pattern with 1, 3, 5, and 7 asterisks


#include <iostream>
using namespace std;


int main()
{
    // Creates an asterisk triangle
    
    cout << "   *" << endl;         // 3 spaces, 1 asterisk
    cout << "  ***" << endl;        // 2 spaces, 3 asterisks
    cout << " *****" << endl;       // 1 space, 5 asterisks
    cout << "*******" << endl;      // 7 asterisks
    
    return 0;
}
